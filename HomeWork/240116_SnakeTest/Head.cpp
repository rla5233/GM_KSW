// 
// 1. 내가 특정 방향으로 진행했다면 다음 입력때는 그 반대방향으로는 갈수가 없다.
//    내가 이동하는 방향의 반대방향으로는 가면 안된다.
// 2. 내가 이동을 해서 CurBody를 획득했다면 그 다음부터 그 바디는 나를 따라와야 한다.

void Head::InputCheck()
{
	int Select = _getch();
	switch (Select)
	{
	case 'A':
	case 'a':
	{
		if (PrevDir != Right)
		{
			PrevDir = Left;
		}
		break;
	}
	case 'S':
	case 's':
	{
		if (PrevDir != Up)
		{
			PrevDir = Down;
		}
		break;
	}
	case 'W':
	case 'w':
	{
		if (PrevDir != Down)
		{
			PrevDir = Up;
		}
		break;
	}
	case 'D':
	case 'd':
	{
		if (PrevDir != Left)
		{
			PrevDir = Right;
		}
		break;
	}
	case '1':
	{
		GetCore()->EngineEnd();
		break;
	}
	default:
		break;
	}
}

void Head::Move()
{
	AddPos(PrevDir);
	PrevPos = GetPos() - PrevDir;
}

void Head::EatBodyCheck()
{
	if (nullptr == BodyManager::GetCurBody())
	{
		MsgBoxAssert("먹을수 있는 바디가 존재하지 않습니다.");
		return;
	}

	Body* CurBody = BodyManager::GetCurBody();
	if (CurBody->GetPos() == GetPos())
	{
		CurBody->SetFront(LastPart);
		LastPart = CurBody;

		BodyManager::ResetBody();
	}
}

void Head::GameEndCheck()
{
	if (GetPos().X < 0 || GetPos().X >= GetCore()->Screen.GetScreenX() ||
		GetPos().Y < 0 || GetPos().Y >= GetCore()->Screen.GetScreenY() )
	{
		GetCore()->EngineEnd();
	}
}

void Head::Update()
{
	Move();
	GameEndCheck();
	EatBodyCheck();

	std::cin.clear();

	int InputCount = _kbhit();
	if (0 == InputCount)
	{
		return;
	}

	InputCheck();
}